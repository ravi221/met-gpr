/**
 * This interface represents an instance of a user contact information.
 */
export interface IUserInfo {
    /**
      * The user's first name.
      */
     firstName: string;
     /**
      * The user's last name.
      */
     lastName: string;
     /**
      * The user's email address.
      */
     emailAddress: string;
     /**
      * The user's METNET Id.
      */
     metnetId: string;
   }
